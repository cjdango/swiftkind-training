def is_equal(a, b):
    """
    Compares a and b, returns True if equal and False if not.
    """
    return a == b

def not_equal(a, b):
    """
    Compares a and b, returns False if equal and True if not.
    """
    return a != b

def left_greater(a, b):
    """
    If the value of left operand is greater than the value of 
    right operand, returns True. False if not.
    """
    return a > b

def left_lesser(a, b):
    """
    If the value of left operand is lesser than the value of 
    right operand, returns True. False if not.
    """
    return a < b

def left_greater_equal(a, b):
    """
    If the value of left operand is greater than or equal to the value of 
    right operand, returns True. False if not.
    """
    return a >= b

def left_lesser_equal(a, b):
    """
    If the value of left operand is lesser than or equal to the value of 
    right operand, returns True. False if not.
    """
    return a <= b